package primary;

public class SwitchCase {
    public static void main(String[]args){

        int test =20;
        switch (test){
            case 10:
                System.out.println("Test grater than 10");
                break;
            case 12:
                System.out.println(" test is grater than 12");
                break;
            case 15:
                System.out.println(" test is grater than 15");
                break;
            case 20:
                System.out.println("Test grater than 20");
                break;
            case 19:
                System.out.println(" test is grater than 18");
                break;
            default:
                System.out.println(" test is grater than 19");


        }
    }
}
